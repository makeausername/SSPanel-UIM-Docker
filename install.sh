warn_existing_config_values() {
    echo "WARNING: 保留现有 ${APP_CONFIG_FILE}，不会修改该文件。" >&2
    echo "WARNING: 请手动确认当前部署模式需要的值：" >&2
    printf "  \$_ENV['baseUrl'] = %s;\n" "$(php_string "$BASE_URL")" >&2
    printf "  \$_ENV['cookie_secure'] = %s;\n" "$COOKIE_SECURE" >&2
    echo "WARNING: 现有 muKey 保持不变。如手动修改 muKey，请同步更新所有节点侧配置。" >&2
}

write_https_compose_override() {
    local temp_file

    if [ -e "$COMPOSE_OVERRIDE_FILE" ] && ! grep -Fq "$COMPOSE_OVERRIDE_MARKER" "$COMPOSE_OVERRIDE_FILE"; then
        if confirm "${COMPOSE_OVERRIDE_FILE} already exists and was not generated by install.sh. Back it up and replace it for HTTPS mode?"; then
            backup_file "$COMPOSE_OVERRIDE_FILE"
        else
            die "Cannot continue in HTTPS mode without managing ${COMPOSE_OVERRIDE_FILE}."
        fi
    fi

    temp_file="$(mktemp)"

    {
        echo "$COMPOSE_OVERRIDE_MARKER"
        echo "services:"
        echo "  caddy:"
        echo "    ports:"
        echo "      - \"\${HTTPS_PORT:-443}:443\""
    } > "$temp_file"

    chmod 0644 "$temp_file"
    mv "$temp_file" "$COMPOSE_OVERRIDE_FILE"
}

remove_https_compose_override() {
    if [ ! -e "$COMPOSE_OVERRIDE_FILE" ]; then
        return 0
    fi

    if grep -Fq "$COMPOSE_OVERRIDE_MARKER" "$COMPOSE_OVERRIDE_FILE"; then
        rm -f "$COMPOSE_OVERRIDE_FILE"
        echo "Removed generated ${COMPOSE_OVERRIDE_FILE}; HTTP-only mode will not publish port 443."
        return 0
    fi

    echo "WARNING: ${COMPOSE_OVERRIDE_FILE} exists and was not generated by install.sh." >&2
    echo "WARNING: HTTP-only base Compose no longer publishes port 443, but your custom override may still change published ports." >&2

    if confirm "Keep custom ${COMPOSE_OVERRIDE_FILE} for HTTP-only mode?"; then
        echo "Keeping custom ${COMPOSE_OVERRIDE_FILE}. Review it manually if HTTP-only mode must not publish port 443."
        return 0
    fi

    if confirm "Back up and remove custom ${COMPOSE_OVERRIDE_FILE} for HTTP-only mode?"; then
        backup_file "$COMPOSE_OVERRIDE_FILE"
        rm -f "$COMPOSE_OVERRIDE_FILE"
        return 0
    fi

    die "Cannot guarantee HTTP-only port behavior while unmanaged ${COMPOSE_OVERRIDE_FILE} remains active."
}

