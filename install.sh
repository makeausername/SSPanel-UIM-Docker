write_config_file() {
    local temp_file
    local key_value
    local mu_key_value
    local app_name_value
    local base_url_value

    key_value="$(php_string "$APP_KEY")"
    mu_key_value="$(php_string "$MU_KEY")"
    app_name_value="$(php_string "$APP_NAME")"
    base_url_value="$(php_string "$BASE_URL")"

    temp_file="$(mktemp "config/.config.php.tmp.XXXXXX")"
    TEMP_FILES+=("$temp_file")

    while IFS= read -r line || [ -n "$line" ]; do
        case "$line" in
            "\$_ENV['key'] ="*) printf "\$_ENV['key'] = %s;     // Generated by install.sh\n" "$key_value" ;;
            "\$_ENV['appName'] ="*) printf "\$_ENV['appName'] = %s;         // Generated by install.sh\n" "$app_name_value" ;;
            "\$_ENV['baseUrl'] ="*) printf "\$_ENV['baseUrl'] = %s; // Generated by install.sh\n\$_ENV['cookie_secure'] = true;        // Generated by install.sh\n" "$base_url_value" ;;
            "\$_ENV['muKey'] ="*) printf "\$_ENV['muKey'] = %s;           // Generated by install.sh\n" "$mu_key_value" ;;
            "\$_ENV['db_host'] ="*) printf "\$_ENV['db_host'] = 'mariadb';\n" ;;
            "\$_ENV['db_socket'] ="*) printf "\$_ENV['db_socket'] = '';\n" ;;
            "\$_ENV['db_database'] ="*) printf "\$_ENV['db_database'] = %s; // Generated by install.sh\n" "$(php_string "$DB_DATABASE")" ;;
            "\$_ENV['db_username'] ="*) printf "\$_ENV['db_username'] = %s;    // Generated by install.sh\n" "$(php_string "$DB_USERNAME")" ;;
            "\$_ENV['db_password'] ="*) printf "\$_ENV['db_password'] = %s; // Generated by install.sh\n" "$(php_string "$DB_PASSWORD")" ;;
            "\$_ENV['db_port'] ="*) printf "\$_ENV['db_port'] = '3306';        // Generated by install.sh\n" ;;
            "\$_ENV['redis_host'] ="*) printf "\$_ENV['redis_host'] = 'redis';    // Generated by install.sh\n" ;;
            "\$_ENV['redis_port'] ="*) printf "\$_ENV['redis_port'] = 6379;           // Generated by install.sh\n" ;;
            "\$_ENV['redis_username'] ="*) printf "\$_ENV['redis_username'] = '';         // Generated by install.sh\n" ;;
            "\$_ENV['redis_password'] ="*) printf "\$_ENV['redis_password'] = %s;         // Generated by install.sh\n" "$(php_string "$REDIS_PASSWORD")" ;;
            "\$_ENV['timeZone'] ="*) printf "\$_ENV['timeZone'] = %s;        // Generated by install.sh\n" "$(php_string "$TZ")" ;;
            *) printf '%s\n' "$line" ;;
        esac
    done < "$APP_CONFIG_EXAMPLE" > "$temp_file"

    chmod 0644 "$temp_file"
    mv -f -- "$temp_file" "$APP_CONFIG_FILE"
}

