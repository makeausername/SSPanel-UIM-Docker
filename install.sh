DB_PASSWORD=""
DB_ROOT_PASSWORD=""
REDIS_PASSWORD=""
APP_KEY=""
MU_KEY=""
CREDENTIALS_FILE=""
RECOVERY_CREDENTIALS_FILE=""
ADMIN_PASSWORD_RECOVERABLE="false"

CURRENT_STAGE="启动安装程序"
INSTALL_SUCCEEDED="false"
FALLBACK_LOCK_HELD="false"
BUILD_STASH_DIR=""
STASHED_PATHS=()
STASHED_FILES=()
TEMP_FILES=()

if [ -t 1 ] && [ -z "${NO_COLOR:-}" ] && [ "${TERM:-}" != "dumb" ]; then
    BLUE=$'\033[34m'
    GREEN=$'\033[32m'
    YELLOW=$'\033[33m'
    RED=$'\033[31m'
    GRAY=$'\033[90m'
    RESET=$'\033[0m'
    INFO_MARK="i"
    OK_MARK="✓"
    WARN_MARK="!"
    ERROR_MARK="✗"
else
    BLUE=""
    GREEN=""
    YELLOW=""
    RED=""
    GRAY=""
    RESET=""
    INFO_MARK="i"
    OK_MARK="OK"
    WARN_MARK="WARN"
    ERROR_MARK="ERROR"
fi

info() {
    printf '%b%s%b %s\n' "$BLUE" "$INFO_MARK" "$RESET" "$*"
}

success() {
    printf '%b%s%b %s\n' "$GREEN" "$OK_MARK" "$RESET" "$*"
}

warn() {
    printf '%b%s%b %s\n' "$YELLOW" "$WARN_MARK" "$RESET" "$*" >&2
}

die() {
    printf '%b%s%b %s\n' "$RED" "$ERROR_MARK" "$RESET" "$*" >&2
    exit 1
}

show_step() {
    CURRENT_STAGE="$2"
    printf '\n%b[%s/8]%b %s\n' "$BLUE" "$1" "$RESET" "$2"
    printf '%b%s%b\n' "$GRAY" '────────────────────────────────────────' "$RESET"
}

banner() {
    if [ "${TERM:-}" = "dumb" ]; then
        cat <<'EOF'
========================================
       EzIPLC 面板智能安装向导
       Secure Docker Deployment
========================================
EOF
        return 0
    fi

    cat <<'EOF'
╭────────────────────────────────────────╮
│       EzIPLC 面板智能安装向导          │
│       Secure Docker Deployment         │
╰────────────────────────────────────────╯
EOF
}

restore_build_files() {
    local index
    local path
    local stash_file

    for index in "${!STASHED_PATHS[@]}"; do
        path="${STASHED_PATHS[$index]:-}"
        stash_file="${STASHED_FILES[$index]:-}"
        if [ -z "$path" ] || [ -z "$stash_file" ]; then
            warn "跳过不完整的构建暂存记录：${index}。"
            continue
        fi
        if [ -e "$stash_file" ]; then
            mkdir -p "$(dirname "$path")"
            mv -- "$stash_file" "$path"
        fi
    done

    if [ -n "$BUILD_STASH_DIR" ] && [ -d "$BUILD_STASH_DIR" ]; then
        rmdir "$BUILD_STASH_DIR" 2>/dev/null || true
    fi

    BUILD_STASH_DIR=""
    STASHED_PATHS=()
    STASHED_FILES=()
}

cleanup_temp_files() {
    local path
    for path in "${TEMP_FILES[@]}"; do
        [ ! -e "$path" ] || rm -f -- "$path"
    done
}

failure_report() {
    printf '\n%b%s 安装失败%b\n' "$RED" "$ERROR_MARK" "$RESET" >&2
    printf '失败阶段：%s\n' "$CURRENT_STAGE" >&2

    if command -v docker >/dev/null 2>&1 && docker compose version >/dev/null 2>&1; then
        printf '\nDocker Compose 状态：\n' >&2
        docker compose ps >&2 2>/dev/null || true
    fi

    if [ -n "$RECOVERY_CREDENTIALS_FILE" ] && [ -f "$RECOVERY_CREDENTIALS_FILE" ]; then
        printf '\n恢复凭据已保留：%s\n' "$RECOVERY_CREDENTIALS_FILE" >&2
        printf '修复问题后可运行：sudo bash bootstrap.sh --resume\n' >&2
    fi

    cat >&2 <<'EOF'

建议的排查命令：
  docker compose ps
  docker compose logs --tail=200 caddy
  docker compose logs --tail=200 app
  docker compose logs --tail=200 mariadb

脚本没有删除任何 Docker volume。修复问题前请保留 .env、config/.config.php、
config/appprofile.php 和现有数据卷。
EOF
}

on_exit() {
    local status=$?
    trap - EXIT

    restore_build_files || true
    cleanup_temp_files || true
    if [ "$FALLBACK_LOCK_HELD" = "true" ]; then
        rmdir "$PROCESS_LOCK_DIR" 2>/dev/null || true
    fi

    if [ "$status" -ne 0 ] && [ "$INSTALL_SUCCEEDED" != "true" ]; then
        failure_report
    fi

    exit "$status"
}

trap on_exit EXIT

require_root() {
    [ "${EUID:-$(id -u)}" -eq 0 ] || die "install.sh 必须以 root 运行。请通过 bootstrap.sh 启动安装。"
}

require_command() {
    command -v "$1" >/dev/null 2>&1 || die "缺少必需命令：$1"
}

require_runtime() {
    require_command docker
    require_command openssl
    require_command curl
    docker compose version >/dev/null 2>&1 || die "需要 Docker Compose v2 插件，请使用 'docker compose'。"
    docker info >/dev/null 2>&1 || die "Docker daemon 不可用，请检查 systemctl status docker。"
    [ -f "$APP_CONFIG_EXAMPLE" ] || die "缺少模板：${APP_CONFIG_EXAMPLE}"
    [ -f "$APP_PROFILE_EXAMPLE" ] || die "缺少模板：${APP_PROFILE_EXAMPLE}"
    [ -f docker-compose.yml ] || die "缺少 docker-compose.yml。"
}

acquire_install_lock() {
    mkdir -p storage

    if command -v flock >/dev/null 2>&1; then
        exec 9>"$PROCESS_LOCK_FILE"
        flock -n 9 || die "检测到另一个安装进程正在运行，本次安装已终止。"
        return 0
    fi

    if ! mkdir "$PROCESS_LOCK_DIR" 2>/dev/null; then
        die "检测到另一个安装进程或遗留锁 ${PROCESS_LOCK_DIR}，请确认没有安装进程后再处理。"
    fi
    FALLBACK_LOCK_HELD="true"
}

compose_has_containers() {
    [ -n "$(DB_PASSWORD=probe DB_ROOT_PASSWORD=probe docker compose ps -aq 2>/dev/null || true)" ]
}

has_mariadb_volume() {
    local project_name

    project_name="$(basename "$INSTALL_DIR" | tr '[:upper:]' '[:lower:]' | sed -E 's/[^a-z0-9_-]+/-/g; s/^[^a-z0-9]+//')"
    [ -n "$(docker volume ls -q \
        --filter "label=com.docker.compose.project=${project_name}" \
        --filter 'label=com.docker.compose.volume=mariadb_data' \
        2>/dev/null || true)" ]
}

has_installation_files() {
    [ -f "$ENV_FILE" ] \
        || [ -f "$APP_CONFIG_FILE" ] \
        || [ -f "$APP_PROFILE_FILE" ] \
        || [ -f "$COMPOSE_OVERRIDE_FILE" ] \
        || [ -f "$INSTALL_LOCK" ]
}

guard_existing_installation() {
    case "$INSTALL_MODE" in
        install)
            if has_installation_files || compose_has_containers || has_mariadb_volume; then
                if [ -f "$ENV_FILE" ] && [ -f "$APP_CONFIG_FILE" ] && [ ! -e "$INSTALL_LOCK" ]; then
                    die "检测到未完成的安装。请保留现有配置和 Docker volume，并使用 bootstrap.sh --resume。"
                fi
                die "检测到已有安装，为避免数据库凭据失配，本次安装已安全终止。请使用 bootstrap.sh --upgrade。"
            fi
            ;;
        reinstall)
            [ "${SSPANEL_REINSTALL_CONFIRMED:-}" = "1" ] \
                || die "重装必须通过 bootstrap.sh --reinstall 的确认流程启动。"
            if compose_has_containers || has_mariadb_volume; then
                die "检测到现有容器或 MariaDB volume。脚本不会自动删除生产数据，请先备份并手工处理。"
            fi
            ;;
        resume)
            [ -f "$ENV_FILE" ] || die "恢复模式要求现有 .env。"
            [ -f "$APP_CONFIG_FILE" ] || die "恢复模式要求现有 config/.config.php。"
            [ ! -e "$INSTALL_LOCK" ] || die "检测到 storage/.install_lock；已完成的安装不能使用 --resume。"
            ;;
        *) die "不支持的安装模式：${INSTALL_MODE}" ;;
    esac
}

trim() {
    local value="$*"
    value="${value#"${value%%[![:space:]]*}"}"
    value="${value%"${value##*[![:space:]]}"}"
    printf '%s' "$value"
}

read_tty() {
    local variable_name="$1"
    local prompt="$2"
    local secret="${3:-false}"
    local input_value=""

    [ -r /dev/tty ] || die "当前会话没有可用的 /dev/tty，无法安全读取交互输入。"

    if [ "$secret" = "true" ]; then
        IFS= read -r -s -p "$prompt" input_value </dev/tty
        printf '\n' >/dev/tty
    else
        IFS= read -r -p "$prompt" input_value </dev/tty
    fi
    printf -v "$variable_name" '%s' "$input_value"
}

prompt_site_name() {
    local value

    while true; do
        read_tty value "站点名称 [EzIPLC]: "
        value="$(trim "$value")"
        APP_NAME="${value:-EzIPLC}"
        [ -n "$APP_NAME" ] && return 0
    done
}

valid_domain() {
    [[ "$1" =~ ^[a-z0-9]([a-z0-9-]{0,61}[a-z0-9])?(\.[a-z0-9]([a-z0-9-]{0,61}[a-z0-9])?)+$ ]]
}

prompt_domain() {
    local value

    while true; do
        read_tty value "站点域名（例如 panel.example.com）: "
        value="$(trim "$value" | tr '[:upper:]' '[:lower:]')"
        if ! valid_domain "$value"; then
            warn "请输入纯域名，不要包含 http://、https://、端口、路径或空格。"
            continue
        fi
        APP_DOMAIN="$value"
        return 0
    done
}

valid_email() {
    [[ "$1" =~ ^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$ ]]
}

prompt_admin_email() {
    local value

    while true; do
        read_tty value "管理员邮箱: "
        value="$(trim "$value")"
        if ! valid_email "$value"; then
            warn "管理员邮箱格式不正确，请重新输入。"
            continue
        fi
        ADMIN_EMAIL="$value"
        return 0
    done
}

prompt_admin_password() {
    local first
    local second

    while true; do
        read_tty first "管理员密码（至少 12 个字符）: " "true"
        if [ "${#first}" -lt 12 ]; then
            warn "管理员密码至少需要 12 个字符。"
            continue
        fi
        read_tty second "再次输入管理员密码: " "true"
        if [ "$first" != "$second" ]; then
            warn "两次输入的密码不一致，请重新输入。"
            continue
        fi
        ADMIN_PASSWORD="$first"
        ADMIN_PASSWORD_RECOVERABLE="true"
        return 0
    done
}

confirm_install() {
    local answer

    printf '\n%b安装确认%b\n' "$BLUE" "$RESET"
    printf '  站点名称：%s\n' "$APP_NAME"
    printf '  站点域名：%s\n' "$APP_DOMAIN"
    printf '  管理员账号：%s\n' "$ADMIN_EMAIL"
    printf '  安装目录：%s\n' "$INSTALL_DIR"
    printf '  HTTPS 模式：Caddy 自动 HTTPS（80/443）\n'
    printf '  时区：%s\n' "$TZ"
    printf '%b所有密码和内部密钥均已隐藏。%b\n' "$GRAY" "$RESET"

    read_tty answer "确认开始安装？[Y/n] "
    case "$(trim "$answer")" in
        ""|y|Y|yes|YES) return 0 ;;
        *)
            info "安装已取消，尚未写入配置或启动容器。"
            exit 0
            ;;
    esac
}

random_hex() {
    local bytes="$1"
    local expected_length="$2"
    local value

    value="$(openssl rand -hex "$bytes")" || die "openssl rand 生成随机值失败。"
    [[ "$value" =~ ^[0-9a-f]+$ ]] || die "随机值包含非法字符。"
    [ "${#value}" -eq "$expected_length" ] || die "随机值长度不符合预期。"
    printf '%s' "$value"
}

validate_hex_value() {
    local label="$1"
    local value="$2"
    local expected_length="$3"

    [ -n "$value" ] || die "${label} 不能为空。"
    [[ "$value" =~ ^[0-9a-f]+$ ]] || die "${label} 只能包含小写十六进制字符。"
    [ "${#value}" -eq "$expected_length" ] || die "${label} 长度不符合预期。"
}

generate_credentials() {
    DB_DATABASE="sspanel_$(random_hex 5 10)"
    DB_USERNAME="ssu_$(random_hex 6 12)"
    DB_PASSWORD="$(random_hex 24 48)"
    DB_ROOT_PASSWORD="$(random_hex 24 48)"
    REDIS_PASSWORD="$(random_hex 24 48)"
    APP_KEY="$(random_hex 32 64)"
    MU_KEY="$(random_hex 32 64)"

    [[ "$DB_DATABASE" =~ ^[a-z0-9_]+$ ]] || die "数据库名包含非法字符。"
    [[ "$DB_USERNAME" =~ ^[a-z0-9_]+$ ]] || die "数据库用户名包含非法字符。"
    [ "${#DB_DATABASE}" -ge 16 ] && [ "${#DB_DATABASE}" -le 20 ] || die "数据库名长度不符合预期。"
    [ "${#DB_USERNAME}" -le 32 ] || die "数据库用户名超过 MariaDB 安全长度。"
    validate_hex_value "数据库密码" "$DB_PASSWORD" 48
    validate_hex_value "数据库 root 密码" "$DB_ROOT_PASSWORD" 48
    validate_hex_value "Redis 密码" "$REDIS_PASSWORD" 48
    validate_hex_value "App Key" "$APP_KEY" 64
    validate_hex_value "muKey" "$MU_KEY" 64

    CADDY_SITE_ADDRESS="$APP_DOMAIN"
    BASE_URL="https://${APP_DOMAIN}"
    success "数据库、Redis、App Key 和 muKey 已使用 OpenSSL 安全生成。"
}

dotenv_quote() {
    local value="$1"
    value="${value//\\/\\\\}"
    value="${value//\'/\\\'}"
    printf "'%s'" "$value"
}

php_string() {
    local value="$1"
    value="${value//\\/\\\\}"
    value="${value//\'/\\\'}"
    printf "'%s'" "$value"
}

atomic_write() {
    local target="$1"
    local mode="$2"
    local directory
    local filename
    local temp_file

    directory="$(dirname "$target")"
    filename="$(basename "$target")"
    mkdir -p "$directory"
    temp_file="$(mktemp "${directory}/.${filename}.tmp.XXXXXX")"
    TEMP_FILES+=("$temp_file")
    cat > "$temp_file"
    chmod "$mode" "$temp_file"
    mv -f -- "$temp_file" "$target"
}

read_env_value() {
    local key="$1"
    local value

    value="$(sed -n "s/^${key}=//p" "$ENV_FILE" | tail -n 1)"
    [ -n "$value" ] || die "现有 .env 缺少 ${key}。"
    case "$value" in
        \'*\') value="${value#\'}"; value="${value%\'}" ;;
        \"*\") value="${value#\"}"; value="${value%\"}" ;;
    esac
    value="${value//\\\'/\'}"
    value="${value//\\\\/\\}"
    printf '%s' "$value"
}

read_php_hex_value() {
    local key="$1"
    local value

    value="$(sed -n "s/^[[:space:]]*\\\$_ENV\['${key}'\][[:space:]]*=[[:space:]]*'\([0-9a-f]*\)'.*/\1/p" "$APP_CONFIG_FILE" | tail -n 1)"
    [ -n "$value" ] || die "现有 config/.config.php 缺少可恢复的 ${key}。"
    printf '%s' "$value"
}

credential_field() {
    local label="$1"
    local source_file="$2"

    sed -n "s/^${label}: //p" "$source_file" | head -n 1
}

find_recovery_credentials_file() {
    local candidate
    local matched=""

    for candidate in /root/eziplc-panel-recovery-*.txt; do
        [ -f "$candidate" ] || continue
        if grep -Fqx "安装目录: ${INSTALL_DIR}" "$candidate"; then
            matched="$candidate"
        fi
    done
    printf '%s' "$matched"
}

load_existing_credentials() {
    local recoverable

    APP_DOMAIN="$(read_env_value APP_DOMAIN)"
    APP_NAME="$(read_env_value APP_NAME)"
    HTTPS_ENABLED="$(read_env_value HTTPS_ENABLED)"
    HTTP_PORT="$(read_env_value HTTP_PORT)"
    HTTPS_PORT="$(read_env_value HTTPS_PORT)"
    CADDY_SITE_ADDRESS="$(read_env_value CADDY_SITE_ADDRESS)"
    DB_DATABASE="$(read_env_value DB_DATABASE)"
    DB_USERNAME="$(read_env_value DB_USERNAME)"
    DB_PASSWORD="$(read_env_value DB_PASSWORD)"
    DB_ROOT_PASSWORD="$(read_env_value DB_ROOT_PASSWORD)"
    REDIS_PASSWORD="$(read_env_value REDIS_PASSWORD)"
    TZ="$(read_env_value TZ)"
    APP_KEY="$(read_php_hex_value key)"
    MU_KEY="$(read_php_hex_value muKey)"
    BASE_URL="https://${APP_DOMAIN}"

    valid_domain "$APP_DOMAIN" || die "现有 .env 中的 APP_DOMAIN 无效。"
    validate_hex_value "数据库密码" "$DB_PASSWORD" 48
    validate_hex_value "数据库 root 密码" "$DB_ROOT_PASSWORD" 48
    validate_hex_value "Redis 密码" "$REDIS_PASSWORD" 48
    validate_hex_value "App Key" "$APP_KEY" 64
    validate_hex_value "muKey" "$MU_KEY" 64

    RECOVERY_CREDENTIALS_FILE="$(find_recovery_credentials_file)"
    if [ -n "$RECOVERY_CREDENTIALS_FILE" ]; then
        recoverable="$(credential_field "管理员密码可恢复" "$RECOVERY_CREDENTIALS_FILE")"
        ADMIN_EMAIL="$(credential_field "管理员账号" "$RECOVERY_CREDENTIALS_FILE")"
        if [ "$recoverable" = "yes" ]; then
            ADMIN_PASSWORD="$(credential_field "管理员密码" "$RECOVERY_CREDENTIALS_FILE")"
            ADMIN_PASSWORD_RECOVERABLE="true"
        fi
    fi

    success "已读取现有配置；数据库、Redis、App Key 和 muKey 均保持不变。"
}

