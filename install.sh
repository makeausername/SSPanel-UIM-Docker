APP_PROFILE_EXAMPLE="config/appprofile.example.php"
APP_CONFIG_FILE="config/.config.php"
APP_PROFILE_FILE="config/appprofile.php"
ENV_FILE=".env"
COMPOSE_OVERRIDE_FILE="docker-compose.override.yml"
COMPOSE_OVERRIDE_MARKER="# Generated by install.sh for SSPanel-UIM Docker HTTPS mode."
INSTALL_LOCK="storage/.install_lock"
PROCESS_LOCK_FILE="storage/.install_in_progress"
PROCESS_LOCK_DIR="storage/.install_in_progress.d"

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]:-.}")" && pwd -P)"
INSTALL_DIR="$SCRIPT_DIR"
INSTALL_MODE="${SSPANEL_INSTALL_MODE:-install}"

HTTPS_ENABLED="true"
HTTP_PORT="80"
HTTPS_PORT="443"
TZ="Asia/Shanghai"
CADDY_SITE_ADDRESS=""
COOKIE_SECURE="true"
BASE_URL=""

APP_NAME="EzIPLC"
APP_DOMAIN=""
ADMIN_EMAIL=""
ADMIN_PASSWORD=""
DB_DATABASE=""
