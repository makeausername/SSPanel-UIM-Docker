generate_secret() {
    if command -v openssl >/dev/null 2>&1; then
        openssl rand -hex 32
        return 0
    fi

    if [ -r /dev/urandom ] && command -v od >/dev/null 2>&1; then
        od -An -N32 -tx1 /dev/urandom | tr -d " \n"
        return 0
    fi

    die "Unable to generate a secure random secret. Install openssl or ensure /dev/urandom and od are available."
}

dotenv_quote() {
    local value="$1"
    value="${value//\\/\\\\}"
    value="${value//\'/\\\'}"
    printf "'%s'" "$value"
}

php_string() {
    local value="$1"
    value="${value//\\/\\\\}"
    value="${value//\'/\\\'}"
    printf "'%s'" "$value"
}

backup_file() {
    local path="$1"
    local backup_path
    backup_path="${path}.bak.$(date +%Y%m%d%H%M%S)"
    cp -p "$path" "$backup_path"
    echo "Backed up ${path} to ${backup_path}"
}

prepare_overwrite() {
    local path="$1"

    if [ -e "$path" ]; then
        if confirm "检测到 ${path} 已存在。是否先备份再覆盖？"; then
            backup_file "$path"
        else
            die "用户选择不覆盖 ${path}，安装已安全停止，未修改该文件。"
        fi
    fi
}

prepare_config_file() {
    CONFIG_WRITE="true"

    if [ -e "$APP_CONFIG_FILE" ]; then
        if confirm "检测到 ${APP_CONFIG_FILE} 已存在。是否先备份再重新生成？"; then
            backup_file "$APP_CONFIG_FILE"
        else
            CONFIG_WRITE="false"
